const phrases = ["ingénieur intelligence artificielle","" ];

const el = document.querySelector(".textScramble");
const fx = new TextScramble(el);

let counter = 0;
const next = () => {
  fx.setText(phrases[counter]).then(() => {
    setTimeout(next, 1500);
  });
  counter = (counter + 1) % phrases.length;
};

next();

var buttonUp = (input, button) => {
  const cards = document.getElementsByClassName("card");
  const filters = document.getElementsByClassName("filter");
  for (let j = 0; j < filters.length; j++) {
    filters[j].classList.remove("selectedButton");
  }
  for (let i = 0; i < cards.length; i++) {
    let title = cards[i].querySelector(".card-body");
    if (input == "All" || title.classList.contains(input)) {
      cards[i].classList.remove("d-none");
      button.classList.add("selectedButton");
    } else {
      cards[i].classList.add("d-none");
    }
  }
};
function validateForm() {
  var name =  document.getElementById('name').value;
  if (name == "") {
      document.querySelector('.status').innerHTML = "Name cannot be empty";
      return false;
  }
  var email =  document.getElementById('email').value;
  if (email == "") {
      document.querySelector('.status').innerHTML = "Email cannot be empty";
      return false;
  } else {
      var re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
      if(!re.test(email)){
          document.querySelector('.status').innerHTML = "Email format invalid";
          return false;
      }
  }
  var subject =  document.getElementById('subject').value;
  if (subject == "") {
      document.querySelector('.status').innerHTML = "Subject cannot be empty";
      return false;
  }
  var message =  document.getElementById('message').value;
  if (message == "") {
      document.querySelector('.status').innerHTML = "Message cannot be empty";
      return false;
  }
  document.querySelector('.status').innerHTML = "Sending...";
}
